def emojify_formula(material_formula):
    """
    Converts a material formula into an emojified version.
    :param material_formula: (str)
    :return:
    (str)
    """
    for element in sorted(list(ELEMENT_TO_EMOJI_MAP.keys()), key=len, reverse=True):
        material_formula = material_formula.replace(
            element, ELEMENT_TO_EMOJI_MAP[element])
    return material_formula


def unemojify_formula(material_formula):
    """
    Converts a material formula into an emojified version.
    :param material_formula: (str)
    :return:
    (str)
    """
    for emoji in EMOJI_TO_ELEMENT_MAP.keys():
        material_formula = material_formula.replace(
            emoji, EMOJI_TO_ELEMENT_MAP[emoji])
    return material_formula
